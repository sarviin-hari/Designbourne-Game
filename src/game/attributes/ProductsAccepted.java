package game.attributes;

/**
 * An enum representing various products that can be purchased by actors in the game.
 * Each constant in this enum represents a different product that an actor can purchase.
 * These products may include items like Bloodberry, HealingVial, RefreshingFlask,
 * Broadsword, GreatKnife, and GiantHammer.
 *
 * Authored by:
 * @author Jun Hirano
 * Modified by:
 * @author Raynen
 * @version 1.0
 */
public enum ProductsAccepted {
    /**
     * Represents the Bloodberry product that can be purchased.
     */
    Bloodberry,

    /**
     * Represents the HealingVial product that can be purchased.
     */
    HealingVial,

    /**
     * Represents the RefreshingFlask product that can be purchased.
     */
    RefreshingFlask,

    /**
     * Represents the Broadsword product that can be purchased.
     */
    Broadsword,

    /**
     * Represents the GreatKnife product that can be purchased.
     */
    GreatKnife,

    /**
     * Represents the GiantHammer product that can be purchased.
     */
    GiantHammer
}
