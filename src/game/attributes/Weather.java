package game.attributes;

/**
 * An enum representing different weather conditions in the game.
 * The weather can be one of the following: SUNNY, RAINY, or NORMAL.
 *
 * @author Raynen Jivash
 * Modified by:
 * @author Sarviin Hari
 * @version 1.0
 */
public enum Weather {
    /**
     * Represents sunny weather condition.
     */
    SUNNY,

    /**
     * Represents rainy weather condition.
     */
    RAINY,

    /**
     * Represents normal weather condition.
     */
    NORMAL
}
